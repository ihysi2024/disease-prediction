public class Adapter {

  /**
   * Regular Adaptor Pattern
   * =======================
   * Extend adaptee class
   * (want to reuse its implementations)
   * and implement target interface
   * (want its implementations too)
   * Basically inheritance of a class and interface
   * COMBINED
   *
   * Object Adaptor Pattern
   * ======================
   * HAS an instance of an adaptee interface object (composition)
   * IMPLEMENTS target interface (inheritance)
   *
   * Two-Way Adapter
   * ===============
   * Implements two interfaces
   */
}
